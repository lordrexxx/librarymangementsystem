    private void addStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentActionPerformed
        // TODO add your handling code here:
        String studID = txtStudentID.getText();
        String lname = txtLname.getText();
        String fname = txtFname.getText();
        String mname = txtMname.getText();
        String month = cmbxMonth.getSelectedItem().toString();
        String date = cmbxDate.getSelectedItem().toString();
        String year = cmbxYear.getSelectedItem().toString();
        String add1 = txtAdd1.getText();
        String add2 = txtAdd2.getText();
        String add3 = txtAdd3.getText();
        String add4 = txtAdd4.getText();
        String gender, address, birthday;
        String syear = cmbxSYear.getSelectedItem().toString();
        String course = cmbxCourse.getSelectedItem().toString();;
        String search;        
        
        if(studID.equals("") || lname.equals("") || fname.equals("") || mname.equals("") || add1.equals("") || add2.equals("") || add3.equals("") || add4.equals("")){
            JOptionPane.showMessageDialog(null, "Fill all the necessary details");
        }
        else if(studID.length()!=5){
            JOptionPane.showMessageDialog(null, "Invalid Student Number");
        }
        else if(add4.length()!=4){
            JOptionPane.showMessageDialog(null, "Invalid Zip Code");
        }
        else{
            if(rbtnMale.isSelected()){
                gender = "Male";
            }
            else if(rbtnFemale.isSelected()){
                gender = "Female";
            }
            else{
                gender = "Non-binary";
            }
            
            address = add1 + " " + add2 + " " + add3 + " " + add4;
            birthday = month + ". " + date + ", " + year;
            
            try{
                
                search = "SELECT `STUDENT ID` FROM `student` WHERE `STUDENT ID` = "+studID+";";
                ResultSet rs = st.executeQuery(search);
                
                if (rs.next()==false){
                    String sql1 = "INSERT INTO `student`(`STUDENT ID`, `LAST NAME`, `FIRST NAME`, `MIDDLE NAME`, `GENDER`, `BIRTHDAY`, `ADDRESS`, `YEAR LEVEL`, `COURSE`)" +" VALUES ('"+ studID +"', '"+ lname +"', '"+ fname +"', '"+ mname +"', '"+ gender +"', '"+ birthday +"', '"+ address +"', '"+ syear +"', '"+ course +"');";
                    st.executeUpdate(sql1);
                    JOptionPane.showMessageDialog(null, "Student Inserted");
                }
                else{
                    JOptionPane.showMessageDialog(null, "This student is already registered.");
                }
                
            }
            catch (SQLException ex){
                Logger.getLogger(addStudent.class.getName()).log(Level.SEVERE,null,ex);
                JOptionPane.showMessageDialog(null, ex);
            }
            
        }
    }//GEN-LAST:event_addStudentActionPerformed

    
