    private void cmbxMonthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbxMonthActionPerformed
        String month = cmbxMonth.getSelectedItem().toString();
        String year = cmbxYear.getSelectedItem().toString();
        int intYear = Integer.parseInt(year);
        
        cmbxDate.removeAllItems();
        
        if(month.equals("Jan") || month.equals("Mar") || month.equals("May") || month.equals("Jul") || month.equals("Aug") || month.equals("Oct") || month.equals("Dec") ){
            for (int i=1; i<=31; i++){
                cmbxDate.addItem(String.valueOf(i));
            }
        }
        else if(month.equals("Apr") || month.equals("Jun") || month.equals("Sept") || month.equals("Nov")){
            for (int i=1; i<=30; i++){
                cmbxDate.addItem(String.valueOf(i));
            }
        }
        else if(month.equals("Feb") && intYear%4==0){
            for (int i=1; i<=29; i++){
                cmbxDate.addItem(String.valueOf(i));
            }
        }
        else if(month.equals("Feb") && intYear%4>=1){
            for (int i=1; i<=28; i++){
                cmbxDate.addItem(String.valueOf(i));
            }
        }
    }//GEN-LAST:event_cmbxMonthActionPerformed

