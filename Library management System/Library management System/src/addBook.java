    private void btnAddCopyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCopyActionPerformed
        String bookID = txtBookID.getText();
        int copies = (Integer) spnrCopy2.getValue();
        int orCopy = 0, newCopy;
        
        if(copies < 1){
            JOptionPane.showMessageDialog(null, "Invalid Number of copies");
        }
        else{
            try{
                
                String search = "SELECT `COPIES` FROM `book` WHERE `BOOK ID` = "+bookID+";";
                ResultSet rs = st.executeQuery(search);
                
                if (bookID.equals("")){
                    JOptionPane.showMessageDialog(null, "Please enter the book ID");
                }
                else if (rs.next()==false){
                    JOptionPane.showMessageDialog(null, "The book is not on the list");
                }
                else{
                    orCopy = rs.getInt("COPIES");
                    newCopy = orCopy + copies;
                    
                    String update = "UPDATE `book` SET `COPIES`='"+newCopy+"' WHERE `BOOK ID`="+bookID+";";
                    st.executeUpdate(update);
                    JOptionPane.showMessageDialog(null, "Copies successfuly added!");
                }
                
            }
            catch (SQLException ex){
                Logger.getLogger(addStudent.class.getName()).log(Level.SEVERE,null,ex);
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_btnAddCopyActionPerformed

