    private void addBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookActionPerformed
        
        String bookID = txtBookID.getText();
        String title = txtTitle.getText();
        String lname = txtLname.getText();
        String fname = txtFname.getText();
        String mname = txtMname.getText();
        int copies = (Integer) spnrCopies.getValue();
        String category = cmbxCat.getSelectedItem().toString();
        String name;
        
        name = lname +", "+fname+" "+mname;
        
        
        
        if (bookID.equals("") || title.equals("") || lname.equals("") || fname.equals("") || mname.equals("")){
            JOptionPane.showMessageDialog(null, "Fill all the necessary details");
        }
        else if(bookID.length()!=5){
            JOptionPane.showMessageDialog(null, "Invalid Book Number");
        }
        else if(copies < 1){
            JOptionPane.showMessageDialog(null, "Invalid Number of copies");
        }
        else{
            try{
                
                String search = "SELECT `BOOK ID` FROM `book` WHERE `BOOK ID` = "+bookID+";";
                ResultSet rs = st.executeQuery(search);
                
                if (rs.next()==false){
                    String sql1 = "INSERT INTO `book`(`BOOK ID`, `TITLE`, `AUTHOR`, `CATEGORY`, `COPIES`) VALUES ('"+ bookID +"', '"+ title +"', '"+ name +"', '"+ category +"', '"+ copies +"');";
                    st.executeUpdate(sql1);
                    JOptionPane.showMessageDialog(null, "Book Inserted");
                }
                else{
                    JOptionPane.showMessageDialog(null, "The book id is already inserted");
                }
                
            }
            catch (SQLException ex){
                Logger.getLogger(addStudent.class.getName()).log(Level.SEVERE,null,ex);
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_addBookActionPerformed

